<div class="pageContent">
    <p>Make a list of announcements and one of them will be randomly chosen every 5 minutes and broadcast to the server.</p>
    <p>Commands: </p>
    <li>/announcements help - will show a help page on how to use this script</li>
    <li>/announcements list - will show a list of all announcements</li>
    <li>/announcements add &lt;text&gt; - adds the message to the announcement list</li>
    <li>/announcements remove &lt;number&gt; - removes the message with the specified id from the announcement list</li>
    <li>/announcements disable - disables the announcements</li>
    <li>/announcements enable - enables the announcements</li>
    <p>You can edit other options from within the script.</p>
    <p>Addons Required:</p>
    <li>SkBee</li>
