    <p>ForceRules will force your players to accept the server rules</p>
    <p>Put your rules command in the config at the top (eg. "/rules")</p>
    <br>
    <p>How to use the /forcerules command:</p>
    <p>/forcerules accept</p>
    <li>This will accept the rules</li>
    <br>
    <p>/forcerules deny</p>
    <li>This will deny the rules</li>
    <br>
    <p>/forcerules check &lt;player&gt;</p>
    <li>This will let you check if a player has accepted the rules</li>
    <br>
    <p>/forcerules reset &lt;player&gt;</p>
    <li>This will reset a player's agreement to the rules</li>
