<div class="pageContent">
    <p>PowerItems is a Spigot plugin that lets you create nice looking items!</p>
    <br>
    <h3>Usage: </h3>
    <p>/createitem &lt;itemID&gt; &lt;itemMaterial&gt; &lt;damage&gt; &lt;rarity&gt; &lt;itemName&gt;</p>
    <p>For rarity see the "Default rarities" section below. For itemName, you can use colour codes. itemMaterial is what the item will be, for example "diamond_sword" or "golden_axe".</p>
    <p>When you create an item using this command, it will be saved to the config file, you can then get more of this item using:</p>
    <p>/getitem &lt;itemID&gt;</p>
    <p>Using /commandtrigger, you can add a command or chat message to the item you are holding that will run when you right click it. Do not include the / when adding a command.</p>
    <p>Example: /commandtrigger add command teleport ^ ^ ^5</p>
    <p>/commandtrigger &lt;add|clear|list&gt; &lt;chat|command&gt; &lt;text&gt;</p>
    <h3>Default rarities:</h3>
    <li>Common</li>
    <li>Uncommon</li>
    <li>Rare</li>
    <li>Epic</li>
    <li>Legendary</li>
    <li>Mythic</li>
    <p>Other plugins can add more rarities using the RarityManager class. <a href="https://github.com/xWires/PowerItemsExampleAddon">Here</a> is an example.</p>
    <br>
    <p>This plugin uses bStats for analytics, the data collected is completely anonymous. If you would like to disable bStats, you can do so in the bStats config file.</p>
    <p>From the bStats website: </p>
    <p>"bStats does not collect any personal data. Most of the collected data is information about the plugin's server like player count, online mode, Minecraft version, Java version, and more. All data gets sent and stored completely anonymously. Server IPs are only stored for ratelimiting and not linked to the data. All data that is collected is publicly visible. If you are interested in what data is collected for your Minecraft server, take a look at the pages for the plugins on your server. Please note that plugin authors can also send data that is customized for their plugin."</p>
